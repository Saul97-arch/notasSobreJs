/* No return você pode atribuir uma string, boolean, uma operação com variáveis o céu é o limite pro JS xD*/

function trueOrFalse(wasThatTrue) {
    
    if(wasThatTrue){
      return "Yes, that was true";
    }
    return "No, that was false";
  
}
  
var res = trueOrFalse(true); //Aqui você guarda o resultado do retorno em uma variável
console.log(res);

console.log(trueOrFalse(false)); //Aqui você mostra o retorno no console